package Class12Homework;

public class ArithmeticOperations {
    public static void main(String[] args) {

int A=50;
int B=61;
System.out.println (A+B); // Add A value to B value : 111
System.out.println (A-B); // Subtract A value from B value : -11
System.out.println (A*B); // Multiply A value to B value : 3,050
System.out.println (A/B); // Divide A value by B value : 0
System.out.println (A%B); // Modulus A value to B value : 50



    }

}

