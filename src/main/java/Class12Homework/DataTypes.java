package Class12Homework;

public class DataTypes {
    public static void main(String[] args) {

        // Data Type Char Example
        char Marks = '8';
        char Grade = 'B';
        {
            System.out.println(Marks);
            System.out.println(Grade);}

        // Data Type Byte Example
        byte myByte1=12;
        byte myByte2=-12;
        {
            System.out.println(myByte1);
            System.out.println(myByte2);
        }
        // Data Type short Example
        short myShort=5000;
        {
            System.out.println(myShort);
        }
        // Data Type int Example
        int i=87;
        {
            System.out.println(i);
        }
        // Data Type Long Example
        long l=10000202039393L;
        {
            System.out.println(l);
        }
        // Data Type float Example
        float weight=56.9f;
        {
            System.out.println(weight);
        }
        // Data Type double Example
        double d=484757.00487d;
        {
            System.out.println(d);
        }
        // Data Type boolean Example
        boolean javaFun=true;
        boolean enjoyJava=false;
        {
            System.out.println(javaFun); //output true
            System.out.println(enjoyJava); //output false
        }
    }
}








