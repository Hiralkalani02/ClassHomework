package Class13Homework;

public class WhileLoopMultiplicationTable {
 public static void main(String[] args) {
     //Print times table using while loop

     int number = 5;
     int i = 1;

     while (i <= 10)
     {
         int result = (number * i);
         System.out.println(5 + "*" + i + "=" + (result));
         i++;
     }


 }
}
